const BY_ID: Record<string, CodingProblem> = Object.fromEntries(PROBLEMS.map((p) => [p.id, p]))

export function getProblem(id: string): CodingProblem {
  // Curated library first, then any user-generated problems (src/lib/coding/customProblems.ts), which
  // carry their own grading hints and so resolve through the same interview + report pipeline.
  return BY_ID[id] ?? loadCustomCodingProblems().find((p) => p.id === id) ?? PROBLEMS[0]
}

/** Lightweight catalog for the JD selector (id + title + statement + topics), curated + generated. */
export function problemCatalog(): Array<{ id: string; title: string; statement: string; topics: string[] }> {
  return [...PROBLEMS, ...loadCustomCodingProblems()].map((p) => ({
    id: p.id,
    title: p.title,
    statement: p.statement,
    topics: p.topics,
  }))
}
