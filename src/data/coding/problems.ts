// Coding/DSA problem library. Each problem is DATA. The `hints` are the hybrid grading reference:
// short, high-signal pointers the interviewer/grader LLM reasons from — the analog of the
// system-design problems' per-stage hints (src/data/sysdesign/problems.ts). They anchor grading for
// THIS problem (the optimal approach + complexity + the traps), not a full answer key.

export interface CodingExample {
  input: string
  output: string
  explanation?: string
}

export interface CodingHints {
  /** What a strong candidate clarifies up front. */
  clarifications: string[]
  /** A correct naive baseline + its complexity. */
  bruteForce: string
  /** The optimal approach: the key insight + data structure. */
  optimal: string
  /** Target complexity of the optimal solution, e.g. "O(n) time, O(n) space". */
  optimalComplexity: string
  /** Edge cases a strong candidate tests. */
  edgeCases: string[]
  /** Common mistakes / traps for this problem. */
  traps: string[]
}

export interface CodingProblem {
  id: string
  title: string
  difficulty: 'Easy' | 'Medium' | 'Hard'
  /** DSA patterns this problem exercises — drives JD ranking and the picker chips. */
  topics: string[]
  statement: string
  examples: CodingExample[]
  constraints: string[]
  hints: CodingHints
}

